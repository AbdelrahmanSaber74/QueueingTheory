    private void enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterActionPerformed
        if(physicalNumberOfServers.getSelectedIndex() == 0 && systemCapacity.getSelectedIndex() == 0)
        {
            M_M_1InputScreen screen = new M_M_1InputScreen();
            screen.setLocationRelativeTo(null);
            screen.setVisible(true);
        }
        else if(physicalNumberOfServers.getSelectedIndex() == 0 && systemCapacity.getSelectedIndex() == 1)
        {
            M_M_1_KInputScreen screen = new M_M_1_KInputScreen();
            screen.setLocationRelativeTo(null);
            screen.setVisible(true);
        }
        else if(physicalNumberOfServers.getSelectedIndex() == 1 && systemCapacity.getSelectedIndex() == 0)
        {
            M_M_cInputScreen screen = new M_M_cInputScreen();
            screen.setLocationRelativeTo(null);
            screen.setVisible(true);
        }
        else
        {
            M_M_c_KInputScreen screen = new M_M_c_KInputScreen();
            screen.setLocationRelativeTo(null);
            screen.setVisible(true);
        }
        this.setVisible(false);
    }//GEN-LAST:event_enterActionPerformed

